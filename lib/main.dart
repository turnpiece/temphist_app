    if (widget.testFuture == null && (!_isAppInitialized || !_splashScreenMinTimeElapsed)) {
      return const SplashScreen();
    }

    if (!_hasSeenOnboarding) {
      return OnboardingScreen(
        onComplete: _completeOnboarding,
