      return;
    }
    
    DebugUtils.logLazy(() => '🔄 Retrying chart data... (attempt ${_chartDataRetryCount + 1}/$_maxChartDataRetries)');
    DebugUtils.logLazy(() => '📋 Failed years to retry: $_failedYears');
    
