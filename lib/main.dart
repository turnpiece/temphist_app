            color: kTextPrimaryColour,
            fontSize: kFontSizeBody,
            fontWeight: FontWeight.w600,
          ),
          overflow: TextOverflow.ellipsis,
        ),
      ],
    );
  }

  void _attachScrollController(ScrollController controller) {
    _activeScrollController?.removeListener(_onActiveScrollChanged);
    _activeScrollController = controller;
    controller.addListener(_onActiveScrollChanged);
    _scrollOffsetNotifier.value =
        controller.hasClients ? controller.offset : 0.0;
  }

  void _onActiveScrollChanged() {
    if (_activeScrollController?.hasClients == true) {
      _scrollOffsetNotifier.value =
          _activeScrollController!.offset.clamp(0.0, double.infinity);
    }
  }

  Color get _locationColour {
    if (!_isLocationDetermined) return kGreyLabelColour.withValues(alpha: 0.4);
    switch (_locationService.locationSource) {
      case LocationSource.gps:
        // Green — your actual GPS location, matches the current-year bar.
        return kBarCurrentYearColour;
      case LocationSource.recentlyVisited:
        // Red — a city you've physically been to (in GPS history).
        // Red has more visual weight than blue, reflecting that personal history
        // is more relevant than an arbitrary popular-city selection.
        return kAccentColour;
      case LocationSource.manual:
        // Blue — a city chosen from the popular list with no GPS history.
        // Neutral, matches the average reference line in the chart.
        return kAverageColour;
    }
  }

  String _buildPeriodHeaderLabel(int pageIndex) {
    final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
    final dateToUse = DateTime.parse(dateInfo['date']!);
    final displayDate = _formatDayMonth(dateToUse);

    switch (pageIndex) {
      case 0:
        return displayDate;
      case 1:
        return 'Week ending $displayDate';
      case 2:
        return 'Month ending $displayDate';
      case 3:
        return 'Year ending $displayDate';
      default:
        return displayDate;
    }
  }

  /// Builds a period page (daily, weekly, monthly, or yearly).
  /// All four periods use the same PeriodPage widget — the only difference
  /// is the periodKey/periodLabel and the daily page additionally wires up
  /// the coachmark callback.
  Widget _buildPeriodPage(
    BuildContext context, {
    required int pageIndex,
    required GlobalKey<PeriodPageState> pageKey,
    required GlobalKey repaintKey,
    required String periodKey,
    required String periodLabel,
    required ScrollController scrollController,
    VoidCallback? onDataLoaded,
  }) {
    final isAtGps = _locationService.isAtGpsCity(_determinedLocation);
    final lat = isAtGps ? _lastPosition?.latitude : null;
    final lon = isAtGps ? _lastPosition?.longitude : null;

    final sidePad = _standardHorizontalPadding();
    final content = Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        RepaintBoundary(
          key: repaintKey,
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Padding(
                padding: EdgeInsets.only(
                  left: sidePad,
                  right: sidePad,
                  bottom: kSectionBottomPadding,
                ),
                child: _buildLocationAndHeadingContent(pageIndex),
              ),
              PeriodPage(
                key: pageKey,
                periodKey: periodKey,
                periodLabel: periodLabel,
                location: _determinedLocation,
                displayLocation:
                    _displayLocation.isNotEmpty ? _displayLocation : null,
                latitude: lat,
                longitude: lon,
                useInternalScroll: false,
                isFahrenheit: _isFahrenheit,
                onDataLoaded: onDataLoaded,
              ),
            ],
          ),
        ),
      ],
    );
    return _buildScrollablePageBody(
      context,
      content,
      scrollController: scrollController,
      wrapWithRefresh: (child) => _buildExternalRefreshIndicator(
        child: child,
        onRefresh: () async {
          await pageKey.currentState?.refresh();
        },
      ),
    );
  }


  Widget _buildDebugPadding(BuildContext context, Widget child) {
    final leftPadding = _standardHorizontalPadding().clamp(0.0, double.infinity);
    final rightPadding = leftPadding;

    return Padding(
      padding: EdgeInsets.only(
        bottom: 8.0, // Reduced bottom padding for debug sections
        left: leftPadding,
        right: rightPadding,
      ),
      child: child,
    );
  }

  double _standardHorizontalPadding() => kScreenPadding + kContentHorizontalMargin;

  /// Start monitoring network connectivity
  void _startConnectivityMonitoring() {
    // Check initial connectivity status asynchronously without blocking
    () async {
      try {
        final initialResults = await Connectivity().checkConnectivity();
        if (mounted) {
          _isOnline = initialResults.any((result) =>
            result == ConnectivityResult.mobile ||
            result == ConnectivityResult.wifi ||
            result == ConnectivityResult.ethernet
          );
          DebugUtils.logLazy(() => '🌐 Initial connectivity status: ${_isOnline ? "online" : "offline"}');
        }
      } catch (e) {
        DebugUtils.logLazy(() => '⚠️ Failed to check initial connectivity: $e');
        // Default to online if check fails (already set to true by default)
      }
    }();

    // Start listening for changes immediately
    _connectivitySubscription = Connectivity().onConnectivityChanged.listen(
      (List<ConnectivityResult> results) {
        final wasOnline = _isOnline;
        _isOnline = results.any((result) =>
          result == ConnectivityResult.mobile ||
          result == ConnectivityResult.wifi ||
          result == ConnectivityResult.ethernet
        );

        DebugUtils.logLazy(() => '🌐 Network connectivity changed: ${_isOnline ? "online" : "offline"}');

        // If we just came back online and were offline, try to refresh data
        if (!wasOnline && _isOnline) {
          DebugUtils.logLazy(() => '🔄 Network restored, attempting to refresh data');
          _handleNetworkRestored();
        }

        // Update UI to reflect connectivity state (_isOnline was updated above)
        if (mounted) {
          DebugUtils.logLazy(() => '🌐 Updating UI for connectivity state: ${_isOnline ? "online" : "offline"}');
          setState(() {
            // _isOnline was already updated above — this triggers a rebuild.
          });
        }
      },
    );
  }

  /// Handle network restoration
  void _handleNetworkRestored() {
    DebugUtils.logLazy(() => '🔄 Network restored, reloading period data');
    _reloadAllPeriodPages();
  }

  /// Perform memory cleanup and data optimization
  /// Called on app lifecycle events (resume/pause/detach) instead of periodic timer
  void _performMemoryCleanup() {
    _memoryCleanupCount++;
    DebugUtils.logLazy(() => '🧹 Performing memory cleanup #$_memoryCleanupCount');

    try {
      _cleanupExpiredCache();
      DebugUtils.logLazy(() => '✅ Memory cleanup completed');
