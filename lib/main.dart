  String _extractDisplayLocation(String fullLocation) {
    if (fullLocation.isEmpty) return '';
    
    // Split by comma and take the first part (city)
    final parts = fullLocation.split(',');
    if (parts.isNotEmpty) {
      return parts.first.trim();
    }
    
    return fullLocation;
  }


  void _updateLoadingMessage() {
    if (!mounted) return;
    
    String newMessage;
    
    // Only show temperature-related messages if location has been determined
    if (!_isLocationDetermined) {
      if (_loadingElapsedSeconds < 5) {
        newMessage = 'Determining your location...';
      } else if (_loadingElapsedSeconds < 15) {
        newMessage = 'Getting your location from GPS...';
      } else if (_loadingElapsedSeconds < 30) {
        newMessage = 'Still determining your location...';
      } else {
        newMessage = 'Location detection is taking longer than expected.';
      }
    } else {
      // Location is determined, show temperature-related messages
      if (_loadingElapsedSeconds < 10) {
        newMessage = 'Loading temperature data...';
      } else if (_loadingElapsedSeconds < 25) {
        // Calculate the date that will be used (same logic as _loadChartData)
        final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
        final dateToUse = DateTime.parse(dateInfo['date']!);
        final friendlyDate = _formatDayMonth(dateToUse);
        newMessage = 'Getting temperatures on $friendlyDate over the past 50 years.';
      } else if (_loadingElapsedSeconds < 45) {
        // Use the display location if available
        final locationText = _displayLocation.isNotEmpty ? _displayLocation : 'your area';
        newMessage = 'Analyzing temperature patterns in $locationText...';
      } else if (_loadingElapsedSeconds < 60) {
        newMessage = 'Calculating averages and trends...';
      } else if (_loadingElapsedSeconds < 80) {
        newMessage = 'Almost there...';
      } else {
        newMessage = 'The server is taking longer than usual. Please wait...';
      }
    }
    
    if (newMessage != _currentLoadingMessage) {
      setState(() {
        _currentLoadingMessage = newMessage;
      });
    }
  }

  Future<void> _loadChartDataProgressive() async {
    // Prevent multiple concurrent loading operations
    if (_isLoadingOperationActive) {
      DebugUtils.logLazy(() => '_loadChartDataProgressive: Already loading, skipping duplicate request');
      return;
    }
    
    // Check network connectivity before attempting to load data
    if (!_isOnline) {
      DebugUtils.logLazy(() => '📡 No network connection, skipping data load');
      setState(() {
        _isDataLoading = false;
        _progressiveLoadingCompleted = true;
      });
      return;
    }
    
    _isLoadingOperationActive = true;
    DebugUtils.logLazy(() => '_loadChartDataProgressive: Starting progressive chart data load');
    
    try {
      final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
      final formattedDate = dateInfo['date']!;
      final dateToUse = DateTime.parse(formattedDate);
      final now = DateTime.now();
      final useYesterday = now.hour < kUseYesterdayHourThreshold;
      DebugUtils.logLazy(() => '_loadChartDataProgressive: Current time: ${now.hour}:${now.minute}, useYesterday: $useYesterday, dateToUse: $formattedDate');

      final service = TemperatureService();
      String city = dateInfo['city']!;
      
      // Update the current data date
      _currentDataDate = dateToUse;
      
      DebugUtils.logLazy(() => '_loadChartDataProgressive: Using determined city: $city');
      final currentYear = dateToUse.year;
      final identifier = formattedDate.substring(5);

      // Use v1 daily endpoint to fetch all years in one call.
      // fetchPeriodData manages its own internal timeouts (async job + sync fallback),
      // so no outer timeout is needed here — the same as the period pages.
      final periodData = await service.fetchPeriodData('daily', city, identifier);

      final chartData = periodData.values
          .map((value) => TemperatureChartData(
                year: value.year.toString(),
                temperature: value.temperature,
                isCurrentYear: value.year == currentYear,
                hasData: true,
              ))
          .toList();

      if (mounted) {
        setState(() {
          _failedYears
            ..clear()
            ..addAll(periodData.metadata?.missingYears.map((m) => m.year) ?? []);

          _chartDataFailed = _failedYears.isNotEmpty;

          _currentData = _createResultMap(
            chartData: chartData,
            averageTemperature: periodData.average.mean,
            trendSlope: periodData.trend.slope,
            summaryText: periodData.summary,
            dateToUse: dateToUse,
            city: city,
          );

          _isDataLoading = false;
          _progressiveLoadingCompleted = true;
        });
      }

      // Stop loading message timer when data loading completes
      _stopLoadingMessageTimer();
      _stopAverageTrendDisplayTimer();

      // Log final results
      final successfulYears = chartData.where((data) => data.hasData).length;
      final totalYears = chartData.length;
      DebugUtils.logLazy(() => '📊 Chart data loading completed: $successfulYears/$totalYears years loaded successfully');
      if (_failedYears.isNotEmpty) {
        DebugUtils.logLazy(() => '❌ Missing years: $_failedYears');
      }

      return;
    } catch (e) {
      DebugUtils.logLazy(() => '_loadChartDataProgressive failed: $e');

      // Stop loading message timer on error
      _stopLoadingMessageTimer();
      _stopAverageTrendDisplayTimer();

      // Build minimal data structure to show error state
      Map<String, dynamic>? minimalData;
      if (_currentData == null) {
        final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
        final dateToUse = DateTime.parse(dateInfo['date']!);
        final city = dateInfo['city']!;
        minimalData = _createResultMap(
          chartData: <TemperatureChartData>[],
          averageTemperature: null,
          trendSlope: null,
          summaryText: null,
          dateToUse: dateToUse,
          city: city,
        );
      }

      if (mounted) {
        setState(() {
          if (e is RateLimitException) {
            _chartDataFailedDueToRateLimit = true;
          } else if (e is TimeoutException) {
            _chartDataTimedOut = true;
          }
          _chartDataFailed = true;
          _isDataLoading = false;
          _progressiveLoadingCompleted = true;
          if (minimalData != null) _currentData = minimalData;
        });
      }

      return;
    } finally {
      // Always reset the loading operation flag and ensure loading is marked as completed
      _isLoadingOperationActive = false;
      _isDataLoading = false;
      _progressiveLoadingCompleted = true;
      _startCoachmarkIfPending();
    }
  }


  /// Handle retry when offline - intelligently decide what to retry
  Future<void> _handleOfflineRetry() async {
    DebugUtils.logLazy(() => '🔄 Offline retry triggered');
    
    // Check if we're actually online now
    if (_isOnline) {
      DebugUtils.logLazy(() => '🌐 Network is now online, resuming data loading');
      
      // If we have partial data, resume loading missing years
      if (_currentData != null && _progressiveLoadingCompleted) {
        DebugUtils.logLazy(() => '📊 Resuming missing years loading');
        await _loadMissingYears();
      } else if (_currentData == null) {
        DebugUtils.logLazy(() => '📊 No data available, doing full refresh');
        await _handleRefresh();
      } else {
        DebugUtils.logLazy(() => '📊 Data loading in progress, no action needed');
      }
    } else {
      DebugUtils.logLazy(() => '📡 Still offline, checking connectivity...');
      
      // Try to detect if we're actually online by making a quick test request
      try {
        final service = TemperatureService();
        final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
        final city = dateInfo['city']!;
        final mmdd = dateInfo['mmdd']!;
        
        // Make a quick test request to see if we're actually online
        await service.fetchSummaryData(city, mmdd).timeout(const Duration(seconds: 5));
        
        // If we get here, we're actually online
        DebugUtils.logLazy(() => '🌐 Connectivity test successful, network is online');
        if (mounted) {
          setState(() {
            _isOnline = true;
          });
        }
        
        // Now resume loading
        if (_currentData != null && _progressiveLoadingCompleted) {
          DebugUtils.logLazy(() => '📊 Resuming missing years loading after connectivity test');
          await _loadMissingYears();
        } else if (_currentData == null) {
          DebugUtils.logLazy(() => '📊 No data available, doing full refresh after connectivity test');
          await _handleRefresh();
        }
        
      } catch (e) {
        DebugUtils.logLazy(() => '📡 Connectivity test failed, still offline: $e');
        // Show a brief message that we're still offline
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
              content: Text('Still no internet connection. Please check your network settings.'),
              duration: Duration(seconds: 2),
            ),
          );
        }
      }
    }
  }






  Future<void> _handleRefresh() async {
    DebugUtils.logLazy(() => 'Pull-to-refresh triggered');

    // Reset error states
    _resetErrorStates();

    // Always clear the in-memory period cache so stale data is not served
    TemperatureService.clearCache();

    // Clear persistent cache in simulation mode to ensure simulation always runs
    if (AppConfig.enableEndpointFailureSimulation) {
      await _clearCache();
    }
    
    // Reset location determination state
    setState(() {
      _isLocationDetermined = false;
      _determinedLocation = '';
      _displayLocation = '';
      _locationDeterminedAt = null;
    });
    
    // Reinitialize the app (determine location then load data)
    await _initializeApp();
    
    // Don't await the future here - let the FutureBuilder handle it
    // This allows the RefreshIndicator to complete its animation
  }

