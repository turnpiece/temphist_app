  /// Called when the user picks a location from the selector sheet.
  Future<void> _onLocationSelected(
      String apiLocation, SelectionMethod selectionMethod) async {
    if (apiLocation == _determinedLocation) return;

    // Submit selection signal to API and record locally for all explicit picks.
    DebugUtils.logLazy(() => '_onLocationSelected: recording "$apiLocation"');
    _recordLocationSelection(apiLocation);

    final currentPage = _pageIndexNotifier.value;
    final currentPeriod = _periodKeys[currentPage];
    DebugUtils.logLazy(
        () => '📍 Location switch: $_determinedLocation → $apiLocation '
            '(on $currentPeriod page, gps: ${_locationService.gpsLocation})');

    // Purge the in-memory service cache so stale data for the old location
    // is never served to the new fetch.
    TemperatureService.clearCache();
    _prefetchGeneration++;
    _pendingSelectionMethod = selectionMethod;

    await _locationService.setManualLocation(apiLocation);

    // PeriodPage widgets detect the location prop change via didUpdateWidget
    // and re-fetch automatically.  Pre-warm caches for other periods.
    if (!mounted) return;
    _prefetchPeriodData();
  }

  BoxDecoration _trendGradientFor(double? gradientFactor) {
    final f = gradientFactor ?? 0.0;
    final double t = f.abs().clamp(0.0, 1.0);
    final Color top, bottom;
    if (f > 0) {
      top = Color.lerp(kBackgroundColour, kBackgroundWarmColour, t)!;
      bottom = Color.lerp(kBackgroundColourDark, kBackgroundCoolColour, t)!;
    } else if (f < 0) {
      top = Color.lerp(kBackgroundColour, kBackgroundCoolColour, t)!;
      bottom = Color.lerp(kBackgroundColourDark, kBackgroundWarmColour, t)!;
    } else {
      top = kBackgroundColour;
      bottom = kBackgroundColourDark;
    }
    return BoxDecoration(
      gradient: LinearGradient(
        begin: Alignment.topCenter,
        end: Alignment.bottomCenter,
        colors: [top, bottom],
      ),
    );
  }

  void _onTrendLoaded(int pageIndex, double gradientFactor) {
    DebugUtils.logLazy(() => 'Trend gradient: page $pageIndex factor=$gradientFactor');
    setState(() {
