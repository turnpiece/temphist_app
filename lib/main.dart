  Future<void> _shareCurrentPeriod(int pageIndex) async {
    setState(() => _isSharing = true);
    try {
      final String periodKey;
      switch (pageIndex) {
        case 0:
          periodKey = 'daily';
        case 1:
          periodKey = 'week';
        case 2:
          periodKey = 'month';
        case 3:
          periodKey = 'year';
        default:
          return;
      }

      final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
      final dateToUse = DateTime.parse(dateInfo['date']!);
      final identifier = dateIdentifier(dateToUse);
      final refYear = dateToUse.year;
      final unit = _isFahrenheit ? 'fahrenheit' : 'celsius';

      final shareService = ShareService();

      final shareUrl = await shareService.createShare(
        location: _determinedLocation,
        period: periodKey,
        identifier: identifier,
        refYear: refYear,
        unit: unit,
      );

      // Clear the spinner before opening the native sheet. The share future
      // may never resolve on iOS if the user dismisses without selecting an
      // action, which would leave _isSharing stuck as true indefinitely.
      if (mounted) setState(() => _isSharing = false);

      await shareService.share(
        shareUrl: shareUrl,
        shareButtonKey: null,
      );
    } catch (e) {
      DebugUtils.logLazy(() => 'Share failed: $e');
      if (mounted) setState(() => _isSharing = false);
    }
  }

