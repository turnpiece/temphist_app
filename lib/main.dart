  Future<void> _initializeApp() async {
    if (widget.testFuture != null) {
      setState(() => _isAppInitialized = true);
      return;
    }

    DebugUtils.verboseLazy(() => 'Initializing app');

    await _cleanupExpiredCache();
    await _determineLocation();

    // Record the current date so we can detect midnight rollovers.
    _lastDateIdentifier = _currentDateIdentifier();

    if (_splashScreenMinTimeElapsed) {
      _onAppInitialized();
    } else {
      _waitForSplashScreenAndInitialize();
    }

    // PeriodPage widgets self-load once _determinedLocation becomes non-empty
    // (via didUpdateWidget). Pre-warm caches for the other periods in the
    // background.
    _prefetchPeriodData();

    _startListeningToLocationChanges();

    DebugUtils.verboseLazy(() => 'App initialization completed - location: $_determinedLocation');
  }

  Future<void> _checkAndRefreshLocationIfNeeded() async {
    if (_locationService.isLocationStale()) {
      await _refreshLocationAndData();
    }
  }

  /// Check if the calendar date has rolled over since last load and reload
  /// all period pages if so (e.g. after midnight when the app is resumed).
  void _checkAndRefreshDataIfDateChanged() {
    if (_lastDateIdentifier.isEmpty) return;
    final current = _currentDateIdentifier();
    if (current != _lastDateIdentifier) {
      DebugUtils.logLazy(() => 'Date changed ($_lastDateIdentifier → $current), reloading all periods');
      _lastDateIdentifier = current;
      _reloadAllPeriodPages();
    } else {
      DebugUtils.logLazy(() => 'Date unchanged ($current), keeping current data');
    }
  }

  /// Computes the MM-DD identifier used for API calls, accounting for the
  /// "use yesterday" threshold.
  String _currentDateIdentifier() {
    final now = DateTime.now();
    final useYesterday = now.hour < kUseYesterdayHourThreshold;
    final dateToUse = useYesterday ? now.subtract(const Duration(days: 1)) : now;
    return dateIdentifier(dateToUse);
  }

  /// Force-reload every PeriodPage via its GlobalKey.
  void _reloadAllPeriodPages() {
    for (final key in [_dailyPageKey, _weekPageKey, _monthPageKey, _yearPageKey]) {
      key.currentState?.reload();
    }
  }

  Future<void> _refreshLocationAndData() async {
    DebugUtils.logLazy(() => 'Refreshing location and data...');

    final previousLocation = _determinedLocation;

    // Mark refreshing but keep location string so period pages don't flash empty
    _locationService.markRefreshing();

    // Determine new location — PeriodPage widgets will auto-reload via
    // didUpdateWidget when the location prop changes.
    await _determineLocation();

    DebugUtils.logLazy(() => 'Location refresh: $previousLocation → $_determinedLocation');
    _prefetchPeriodData();
  }

  // Cache expiration constants
  static const Duration _currentDateCacheExpiration = Duration(hours: 1);
  static const Duration _historicalDataCacheExpiration = Duration(hours: 24);
  static const Duration _summaryCacheExpiration = Duration(hours: 1);
  static const Duration _averageTrendCacheExpiration = Duration(hours: 6);

  Future<void> _clearCache() async {
    await _safeSharedPreferencesOperation(() async {
      final prefs = await SharedPreferences.getInstance();
      
      // Get all keys to find cache entries
      final keys = prefs.getKeys();
      
      // Clear chart data cache
      await prefs.remove('cachedChartData');
      
      // Clear API response caches (average, trend, summary)
      for (final key in keys) {
        if (key.startsWith('api_')) {
          await prefs.remove(key);
        }
      }
      
      // Clear temperature data caches
      for (final key in keys) {
        if (key.startsWith('tempData_')) {
          await prefs.remove(key);
        }
      }
      
      // Clear location cache
      await prefs.remove('cachedLocation');
      
      DebugUtils.logLazy(() => 'All cache cleared: chart data, API responses, temperature data, and location');
    }, 'clear cache');
  }

  /// Clean up expired cache entries
  Future<void> _cleanupExpiredCache() async {
    await _safeSharedPreferencesOperation(() async {
      final prefs = await SharedPreferences.getInstance();
      final keys = prefs.getKeys();
      final now = DateTime.now();
      int cleanedCount = 0;

      for (final key in keys) {
        if (key.startsWith('tempData_') || key.startsWith('api_')) {
          try {
            final cachedData = prefs.getString(key);
            if (cachedData != null) {
              final data = jsonDecode(cachedData) as Map<String, dynamic>;
              final timestamp = DateTime.fromMillisecondsSinceEpoch(data['timestamp'] as int);
              final age = now.difference(timestamp);
              
              Duration expiration;
              if (key.startsWith('tempData_')) {
                // Temperature data cache
                final year = data['year'] as int;
                final isCurrentYear = year == now.year;
                expiration = isCurrentYear ? _currentDateCacheExpiration : _historicalDataCacheExpiration;
              } else if (key.startsWith('api_')) {
                // API response cache
                final endpoint = data['endpoint'] as String;
                switch (endpoint) {
                  case 'summary':
                    expiration = _summaryCacheExpiration;
                    break;
                  case 'average':
                  case 'trend':
                    expiration = _averageTrendCacheExpiration;
                    break;
                  default:
                    expiration = Duration(hours: 1);
                }
              } else {
                continue; // Skip unknown cache types
              }

              if (age > expiration) {
                await prefs.remove(key);
                cleanedCount++;
              }
            }
          } catch (e) {
            // If we can't parse the data, remove it
            await prefs.remove(key);
            cleanedCount++;
          }
        }
      }

      if (cleanedCount > 0) {
        DebugUtils.logLazy(() => '🧹 Cleaned up $cleanedCount expired cache entries');
      }
    }, 'cleanup expired cache');
  }

  /// Check whether the onboarding flow should be shown (first launch only).
  Future<void> _checkOnboarding() async {
    if (kDebugMode) {
      await _safeSharedPreferencesOperation(() async {
        final prefs = await SharedPreferences.getInstance();
        await prefs.remove('has_seen_onboarding');
      }, 'reset onboarding flag');
    }

    final seen = await _safeSharedPreferencesOperation<bool>(() async {
      final prefs = await SharedPreferences.getInstance();
      return prefs.getBool('has_seen_onboarding') ?? false;
    }, 'load onboarding flag');

    if (seen == true) {
      if (mounted) {
        _onboardingCheckComplete = true;
        _maybeAutoOpenLocationSelector();
      }
      return;
    }

    if (mounted) {
      setState(() => _hasSeenOnboarding = false);
      _onboardingCheckComplete = true;
      // _hasSeenOnboarding is false here so _maybeAutoOpenLocationSelector will
      // not open the selector now; _completeOnboarding will call it instead.
    }
  }

  /// Mark onboarding as complete and return to the main screen.
  Future<void> _completeOnboarding() async {
    await _safeSharedPreferencesOperation(() async {
      final prefs = await SharedPreferences.getInstance();
      await prefs.setBool('has_seen_onboarding', true);
    }, 'save onboarding flag');

    if (mounted) {
      setState(() => _hasSeenOnboarding = true);
      // Now that the main screen is visible, start the coachmark if it was
      // held back because data loaded while onboarding was still showing.
      _startCoachmarkIfPending();
      // If the app had no location when it started, open the selector now
      // that the user has finished onboarding.
      _maybeAutoOpenLocationSelector();
    }
  }

  /// Check whether the swipe coachmark should be shown (first launch only).
  Future<void> _checkSwipeCoachmark() async {
    // In debug builds, reset so the coachmark is always visible for testing.
    if (kDebugMode) {
      await _safeSharedPreferencesOperation(() async {
        final prefs = await SharedPreferences.getInstance();
        await prefs.remove('swipeCoachmarkShown');
      }, 'reset swipe coachmark flag');
    }

    final shown = await _safeSharedPreferencesOperation<bool>(() async {
      final prefs = await SharedPreferences.getInstance();
      return prefs.getBool('swipeCoachmarkShown') ?? false;
    }, 'load swipe coachmark flag');

    if (shown == true) return;

    // Mark as pending — will be shown once the chart finishes loading.
    if (mounted) _coachmarkPending = true;
  }

  /// Show the coachmark now and start the auto-dismiss timer.
  /// Called via PeriodPage's onDataLoaded callback and from
  /// [_completeOnboarding].  Does nothing if the user is still in the
  /// onboarding flow — the coachmark stays pending and is started once
  /// onboarding completes.
  void _startCoachmarkIfPending() {
    if (!_coachmarkPending) return;
    if (!_hasSeenOnboarding) return;
    _coachmarkPending = false;
    if (mounted) {
      setState(() {
        _showSwipeCoachmark = true;
        _coachmarkFadingOut = false;
      });
      Future.delayed(const Duration(seconds: 8), _dismissCoachmark);
    }
  }

  /// Fade out and permanently dismiss the swipe coachmark.
  void _dismissCoachmark() {
    if (!_showSwipeCoachmark || _coachmarkFadingOut) return;
    if (mounted) {
      setState(() {
        _coachmarkFadingOut = true;
      });
    }
    _safeSharedPreferencesOperation(() async {
      final prefs = await SharedPreferences.getInstance();
      await prefs.setBool('swipeCoachmarkShown', true);
    }, 'save swipe coachmark shown');
  }


  Future<void> _determineLocation() async {
    await _locationService.determineLocation();
  }

  /// Pre-warm the in-memory and Hive caches for all four periods in parallel.
  /// PeriodPage widgets that have already loaded will hit the in-memory cache
  /// instead of making a duplicate network call.
  Future<void> _prefetchPeriodData() async {
    if (!_isLocationDetermined || _determinedLocation.isEmpty) return;

    final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
    final identifier = dateInfo['mmdd']!;
    final location = _determinedLocation;
    final service = TemperatureService();
    const periods = ['daily', 'week', 'month', 'year'];

    final generation = _prefetchGeneration;
    bool isCancelled() => _prefetchGeneration != generation;

    final isAtGps = _locationService.isAtGpsCity(location);
    final lat = isAtGps ? _lastPosition?.latitude : null;
    final lon = isAtGps ? _lastPosition?.longitude : null;

    DebugUtils.logLazy(() => 'Prefetching period data in parallel for $location ($identifier)');

    await Future.wait(
      periods.map((period) async {
        if (isCancelled()) return;
        try {
          DebugUtils.logLazy(() => 'Prefetching $period data');
          final unitGroup = _isFahrenheit ? 'fahrenheit' : null;
          final data = await service.fetchPeriodData(
            period,
            location,
            identifier,
            unitGroup: unitGroup,
            isCancelled: isCancelled,
          );
          if (!isCancelled() && lat != null && lon != null) {
            await PeriodCacheService.put(period, lat, lon, identifier, data,
                unitGroup: unitGroup);
          }
        } catch (e) {
          DebugUtils.logLazy(() => 'Prefetch $period failed: $e');
        }
      }),
    );
  }

  void _startSplashScreenTimer() {
    // Skip splash screen timer in test mode
    if (widget.testFuture != null) {
      _splashScreenMinTimeElapsed = true;
      if (!_splashCompleter.isCompleted) _splashCompleter.complete();
      return;
    }

    // Show splash screen for minimum 2 seconds to prevent white flash
    _splashScreenTimer = Timer(const Duration(seconds: 2), () {
      if (mounted) {
        setState(() {
          _splashScreenMinTimeElapsed = true;
        });
        if (!_splashCompleter.isCompleted) _splashCompleter.complete();
      }
    });
  }

  Future<void> _waitForSplashScreenAndInitialize() async {
    // Skip in test mode
    if (widget.testFuture != null) {
      setState(() {
        _isAppInitialized = true;
      });
      return;
    }

    await _splashCompleter.future;
    if (mounted) {
      _onAppInitialized();
    }
  }

  /// Sets [_isAppInitialized] then checks whether the location selector should
