/// Helper function to get current date and location for API calls
/// Returns a map with 'date', 'mmdd', and 'city' keys
Map<String, String> _getCurrentDateAndLocation(String determinedLocation) {
  final now = DateTime.now();
  final useYesterday = now.hour < kUseYesterdayHourThreshold;
  final dateToUse = useYesterday ? now.subtract(Duration(days: 1)) : now;
  final mmdd = DateFormat('MM-dd').format(dateToUse);
  final city = determinedLocation.isNotEmpty ? determinedLocation : kDefaultLocation;
  
  return {
    'date': DateFormat('yyyy-MM-dd').format(dateToUse),
    'mmdd': mmdd,
    'city': city,
  };
}

/// Clean up and validate location strings for better API compatibility
String _cleanupLocationString(String location) {
  if (location.isEmpty) return kDefaultLocation;
  
  // Remove extra whitespace and normalize commas
  String cleaned = location.trim();
  
  // Replace multiple spaces with single space
  cleaned = cleaned.replaceAll(RegExp(r'\s+'), ' ');
  
  // Normalize comma spacing (ensure space after comma)
  cleaned = cleaned.replaceAll(RegExp(r',\s*'), ', ');
  
  // Remove trailing comma if present
  cleaned = cleaned.replaceAll(RegExp(r',\s*$'), '');
  
  // Ensure we have at least a city and country
  if (!cleaned.contains(',')) {
    // If no comma, assume it's just a city, add default country
    cleaned = '$cleaned, UK';
  }
  
  return cleaned;
}

/// Check available storage space and return true if sufficient space is available
Future<bool> _checkStorageSpace() async {
  try {
    if (io.Platform.isAndroid || io.Platform.isIOS) {
      // For mobile platforms, we can't easily check storage space
      // but we can try to write a small test file to see if storage is available
      final tempDir = io.Directory.systemTemp;
      final testFile = io.File('${tempDir.path}/temp_storage_test.tmp');
      
      try {
        await testFile.writeAsString('test');
        await testFile.delete();
        return true;
      } catch (e) {
        DebugUtils.logLazy(() => 'Storage test failed: $e');
        return false;
      }
    } else {
      // For desktop platforms, check available disk space
      final tempDir = io.Directory.systemTemp;
      await tempDir.stat(); // Basic check - if we can access temp dir, assume space is available
      return true;
    }
  } catch (e) {
    DebugUtils.logLazy(() => 'Storage space check failed: $e');
    return false;
  }
}

/// Safely execute SharedPreferences operations with storage space checks
Future<T?> _safeSharedPreferencesOperation<T>(
  Future<T> Function() operation,
  String operationName,
) async {
  try {
    // Check storage space before attempting operation
    final hasSpace = await _checkStorageSpace();
    if (!hasSpace) {
      DebugUtils.logLazy(() => '⚠️ Insufficient storage space for $operationName');
      return null;
    }
    
    return await operation();
  } catch (e) {
    DebugUtils.logLazy(() => '❌ SharedPreferences $operationName failed: $e');
    
    // If it's a storage-related error, try to clear some cache
    if (e.toString().contains('No space left') || 
        e.toString().contains('ENOSPC') ||
        e.toString().contains('storage')) {
      DebugUtils.logLazy(() => '🧹 Storage error detected, attempting cache cleanup');
      await _emergencyCacheCleanup();
      
      // Try the operation once more after cleanup
      try {
        return await operation();
      } catch (e2) {
        DebugUtils.logLazy(() => '❌ Operation still failed after cleanup: $e2');
        return null;
      }
    }
    
    return null;
  }
}

/// Emergency cache cleanup when storage is full
Future<void> _emergencyCacheCleanup() async {
  try {
    final prefs = await SharedPreferences.getInstance();
    final keys = prefs.getKeys();
    int cleanedCount = 0;
    
    // Remove oldest cache entries first (those with oldest timestamps)
    final cacheEntries = <String, int>{};
    
    for (final key in keys) {
      if (key.startsWith('tempData_') || key.startsWith('api_') || key.startsWith('cachedLocation')) {
        try {
          final data = prefs.getString(key);
          if (data != null) {
            final json = jsonDecode(data) as Map<String, dynamic>;
            final timestamp = json['timestamp'];
            if (timestamp is int) {
              cacheEntries[key] = timestamp;
            }
          }
        } catch (e) {
          // If we can't parse the data, remove it immediately
          await prefs.remove(key);
          cleanedCount++;
        }
      }
    }
    
    // Sort by timestamp (oldest first) and remove oldest 50% of entries
    final sortedEntries = cacheEntries.entries.toList()
      ..sort((a, b) => a.value.compareTo(b.value));
    
    final entriesToRemove = (sortedEntries.length * 0.5).ceil();
    for (int i = 0; i < entriesToRemove && i < sortedEntries.length; i++) {
      await prefs.remove(sortedEntries[i].key);
      cleanedCount++;
    }
    
    DebugUtils.logLazy(() => '🧹 Emergency cleanup removed $cleanedCount cache entries');
  } catch (e) {
    DebugUtils.logLazy(() => '❌ Emergency cache cleanup failed: $e');
  }
}

/// Detect suspicious or obviously incorrect location strings
/// This is very conservative since location data comes from device GPS/geocoding
bool _isLocationSuspicious(String location) {
  if (location.isEmpty) return true;
  
  // Check for very short or very long location strings (indicates API issues)
  if (location.length < 2 || location.length > 200) {
    return true;
  }
  
  // Check for pure numeric strings (postal codes without city names)
  if (RegExp(r'^\d+$').hasMatch(location.trim())) {
    return true;
  }
  
  return false;
}




