        () => '🧹 Performing memory cleanup #$_memoryCleanupCount');

    try {
      _cleanupExpiredCache();
      DebugUtils.logLazy(() => '✅ Memory cleanup completed');
    } catch (e) {
      DebugUtils.logLazy(() => '❌ Memory cleanup failed: $e');
    }
