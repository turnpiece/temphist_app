  /// Check if error message indicates storage space issue
  bool _isStorageSpaceError(String message) {
    return message.toLowerCase().contains('storage') ||
           message.toLowerCase().contains('space') ||
           message.toLowerCase().contains('enospc') ||
           message.toLowerCase().contains('no space');
  }

  /// Check if error message indicates location permission issue
  bool _isLocationPermissionError(String message) {
    return message.toLowerCase().contains('location') ||
           message.toLowerCase().contains('permission') ||
           message.toLowerCase().contains('gps') ||
           message.toLowerCase().contains('geolocation');
  }

  /// Check if error message indicates rate limit issue
  bool _isRateLimitError(String message) {
    return message.toLowerCase().contains('rate limit') ||
           message.toLowerCase().contains('too many') ||
           message.toLowerCase().contains('429');
  }

  /// Check if error message indicates network issue
  bool _isNetworkError(String message) {
    final lowerMessage = message.toLowerCase();
    // Only consider it a network error if it's clearly a connectivity issue
    // and not just a temporary API failure
    return (lowerMessage.contains('network') && 
            (lowerMessage.contains('unreachable') || lowerMessage.contains('unavailable'))) ||
           (lowerMessage.contains('connection') && 
            (lowerMessage.contains('refused') || lowerMessage.contains('reset'))) ||
           lowerMessage.contains('no internet') ||
           lowerMessage.contains('offline');
  }

