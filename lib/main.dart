      setState(() {
        _isRetryingChartData = false;
      });
    }
  }

  // if years didn't load before try loading them again
  Future<void> _retryFailedYears() async {
    if (_failedYears.isEmpty) {
      debugPrintIfDebugging('‚ö†Ô∏è No failed years to retry');
      return;
    }
    
    debugPrintIfDebugging('üîÑ Retrying failed years: $_failedYears');
    
    try {
      final dateInfo = _getCurrentDateAndLocation(_determinedLocation);
      final mmdd = dateInfo['mmdd']!;
      final city = dateInfo['city']!;
      
      debugPrintIfDebugging('üìç Retry parameters - city: $city, date: $mmdd');
      
      final service = TemperatureService();
      final currentData = _currentData;
      if (currentData == null) {
        debugPrintIfDebugging('‚ùå No current data available for retry');
        return;
      }
      
      List<TemperatureChartData> chartData = List<TemperatureChartData>.from(currentData['chartData']);
      int successCount = 0;
      
      // Retry each failed year
      for (int year in _failedYears) {
        final dateForYear = '$year-$mmdd';
        debugPrintIfDebugging('üîÑ Retrying year $year ($dateForYear)');
        try {
          final tempData = await service.fetchTemperature(city, dateForYear);
          final temperature = tempData.temperature ?? tempData.average?.temperature;
          
          if (temperature != null) {
            // Find and update the entry for this year
            final yearIndex = chartData.indexWhere((data) => data.year == year.toString());
            if (yearIndex != -1) {
              chartData[yearIndex] = TemperatureChartData(
                year: year.toString(),
                temperature: temperature,
                isCurrentYear: year == DateTime.now().year,
                hasData: true,
              );
              
              successCount++;
              debugPrintIfDebugging('‚úÖ Successfully retried year $year: ${temperature.toStringAsFixed(1)}¬∞C');
            }
          } else {
            debugPrintIfDebugging('‚ùå No valid temperature data for retry year $year');
          }
        } catch (e) {
          debugPrintIfDebugging('‚ùå Failed to retry year $year: $e');
          if (e is RateLimitException) {
            debugPrintIfDebugging('‚ö†Ô∏è Rate limit hit during retry, stopping');
            setState(() {
              _chartDataFailedDueToRateLimit = true;
            });
            break;
          }
        }
        
        // Add a small delay between retries
        await Future.delayed(const Duration(milliseconds: 200));
      }
      
      // Update the current data with retried results
      _currentData!['chartData'] = chartData;
      
      debugPrintIfDebugging('üìä Retry completed: $successCount/${_failedYears.length} years successfully retried');
      
      if (mounted) {
        setState(() {});
      }
      
    } catch (e) {
      debugPrintIfDebugging('‚ùå Error during failed years retry: $e');
    }
  }

